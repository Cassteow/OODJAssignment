    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        String searchID = SearchTxtBox.getText();
        //Empty search bar
        if(searchID == ""){
            JOptionPane.showMessageDialog(null, "Search Bar is Blank! Please Enter Vaccine ID", 
                    "Error",JOptionPane.WARNING_MESSAGE);
        }
        else{
            People ppl = new People();
            //Call search and view vaccine method from Personnel Class
            boolean found = ppl.viewVaccine(searchID);
            //No record found
            if(found == false){
                JOptionPane.showMessageDialog(null, "No record found!", 
                    "Error",JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

