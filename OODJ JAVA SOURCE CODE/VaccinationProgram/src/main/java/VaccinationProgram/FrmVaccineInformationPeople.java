    public FrmVaccineInformationPeople(String id, String n) {
        this.frmAccID = id;
        this.name = n;
        initComponents();
        
        //Load all selections of vaccines to the Combo Box
        vaccineSelectCmbBox.removeAllItems();
        try{
            File file = new File("Vaccine.txt");
            BufferedReader br = new BufferedReader(new FileReader(file));
            String line;
            while((line = br.readLine())!= null){
                String[] vaccine = line.split(";");
                vaccineSelectCmbBox.addItem(vaccine[1]);
            }
            br.close();
        }
        catch(IOException ex){
            JOptionPane.showMessageDialog(null, "There is an error in the system!\nPlease try again later.", "Error",JOptionPane.WARNING_MESSAGE);
        }
    }
    
